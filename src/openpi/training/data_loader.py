class DataLoaderImpl(DataLoader):
    def __init__(self, data_config: _config.DataConfig, data_loader: TorchDataLoader | RLDSDataLoader):
        self._data_config = data_config
        self._data_loader = data_loader

    def data_config(self) -> _config.DataConfig:
        return self._data_config

    def __iter__(self):
        for batch in self._data_loader:
            yield _model.Observation.from_dict(batch), batch["actions"]


class TrajectoryReplayBuffer:
    """Fixed‑capacity per‑task FIFO buffers.

    Each entry is a *single* batch (dict of numpy / jax arrays) already
    transformed & ready for the model. Storing batches avoids expensive re‑run
    of transforms on every replay sample.
    """

    def __init__(self, n_tasks: int, n_memories: int):
        self._n_tasks = n_tasks
        self._n_memories = n_memories    
        self._buffers = [deque(maxlen=n_memories) for _ in range(n_tasks)]

    def add_batch(self, task_id: int, batch: dict[str, Any]):
        self._buffers[task_id].append(batch)

    def build_replay_iter(self, *, shuffle: bool = True):
        """Yield batches indefinitely, uniformly across stored tasks."""
        rng = random.Random()
        while True:
            task_ids = [tid for tid, buf in enumerate(self._buffers) if buf]
            if not task_ids:
                raise StopIteration("Replay buffer is empty — call after first task")
            # Choose a random task then a random batch within that task.
            tid = rng.choice(task_ids)
            buf = self._buffers[tid]
            idx = rng.randrange(len(buf)) if shuffle else 0
            yield buf[idx]
            
    def state_dict(self) -> dict:
        """Return a PyTree friendly structure for checkpointing."""
        
        return {
            "n_tasks": self._n_tasks,
            "n_memories": self._n_memories,
            "buffers": [list(buf) for buf in self._buffers]
        }

    def load_state_dict(self, state: dict):
        """Restore internal storage from a state_dict()."""
        self._n_tasks    = state["n_tasks"]
        self._n_memories = state["n_memories"]

        def _restore_batch(batch):
            obs, act = batch
            # import pdb; pdb.set_trace()
            if isinstance(obs, dict):               # ← convert if needed
                obs = _model.Observation.from_dict(obs)
            return (obs, act)

        self._buffers = [
            deque(
                [_restore_batch(b) for b in task_buf],  # convert each batch
                maxlen=self._n_memories,
            )
            for task_buf in state["buffers"]
        ]

