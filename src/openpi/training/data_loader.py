def _concat(a, b):
    # assumes leading dim = batch dim
    return jax.tree.map(lambda x, y: jnp.concatenate([x, y], axis=0), a, b)


class MixedDataLoader:
    """
    Wrap an existing `DataLoader` to concatenate replay batches.
    """

    def __init__(self, data_config, current_loader, replay_loader):
        self._current_loader = current_loader
        self._replay_loader = replay_loader
        self._data_config = data_config

    def data_config(self) -> _config.DataConfig:
        return self._data_config
    
    def _infinite(self,dataloader):
        """Yield from `dataloader` forever without caching batches."""
        while True:
            for batch in dataloader:
                yield batch

    def __iter__(self):
        replay_iter = self._infinite(self._replay_loader)  # ← no caching
        for cur_obs, cur_act in self._current_loader:
            rep_obs, rep_act = next(replay_iter)
            yield _concat(cur_obs, rep_obs), _concat(cur_act, rep_act)
